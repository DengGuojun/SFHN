package com.lpmas.sfhn.portal.invoker.bean;

public class ClassroomLiveStatusBean {

	private String uid = "";
	private String classid = "";
	private String interval = "";
	private String sourceid = "";
	private String streamurl = "";
	private String playurl = "";
	private String thirdClassroomId = "";

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getClassid() {
		return classid;
	}

	public void setClassid(String classid) {
		this.classid = classid;
	}

	public String getInterval() {
		return interval;
	}

	public void setInterval(String interval) {
		this.interval = interval;
	}

	public String getSourceid() {
		return sourceid;
	}

	public void setSourceid(String sourceid) {
		this.sourceid = sourceid;
	}

	public String getStreamurl() {
		return streamurl;
	}

	public void setStreamurl(String streamurl) {
		this.streamurl = streamurl;
	}

	public String getPlayurl() {
		return playurl;
	}

	public void setPlayurl(String playurl) {
		this.playurl = playurl;
	}

	public String getThirdClassroomId() {
		return thirdClassroomId;
	}

	public void setThirdClassroomId(String thirdClassroomId) {
		this.thirdClassroomId = thirdClassroomId;
	}
	
	

}
